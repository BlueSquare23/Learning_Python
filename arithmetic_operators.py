#!/usr/bin/python3
#Basic Arithmetic Operators

print(2 + 4)  #addition
print(5 - 3)  #subtraction
print(3 - -2) #subtraction w/ negative
print(3 * 8)  #multiplication
print(2 ** 4) #exponentiation
print(6 / 2)  #division
print(7 / 3)  #float division
print(7 // 3) #int division
print(7 % 3)  #modulus (remainder)
